package codein;
public class EncapsulationDemo 
{
private int age;
private String name;
private int roll;
public int getAge()
{
return age;
}
public String getName()
{
return name;
}
public int getRoll()
{
return roll;
}
public void setAge(int newAge)
{
age=newAge;
}
public void setName(String newName)
{
name=newName;
}
public void setRoll(int newRoll)
{
roll=newRoll;
}
}
