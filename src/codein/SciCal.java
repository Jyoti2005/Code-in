    private void jButton34ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton34ActionPerformed
        // TODO add your handling code here:
        n2=Float.parseFloat(jTextField1.getText());
        if(opr.equals("+"))
        {
            res=n1+n2;
        jTextField1.setText(""+res);
        }
        else if(opr.equals("-"))
        {
            res=n1+n2;
        jTextField1.setText(""+res);
        }
        else if(opr.equals("*"))
        {
            res=n1*n2;
        jTextField1.setText(""+res);
        }
        else if(opr.equals("/"))
        {
            res=n1/n2;
        jTextField1.setText(""+res);
        }
        else if(opr.equals("%"))
        {
        res=n1*n2; // n1/100   0.n1*n2    n1/100*n2
        jTextField1.setText(""+res);
        }
        else if(opr.equals("^"))
        {
         jTextField1.setText(""+ Math.pow(n1,n2));
         
        }
        else if(opr.equals(""))
        {
        jTextField1.setText("");
        JOptionPane.showMessageDialog(null,"No operation selected!");
        }
        jLabel1.setText("");
        opr="";
    }//GEN-LAST:event_jButton34ActionPerformed

